package JavaBasico1;

public class Ejercicio1 {
    public static void main(String[] args) throws Exception {
        System.out.println("Ejercicio 1 de curso Java Básico, imprimiendo tipos de datos: ");
        //Numéricos
        byte a =1;
        short b = 2;
        int c = 3;
        long d = 4;
        float e = 4.1f;
        double f = 5.2;
        //booleano
        boolean g = true;
        //caracteres
        char h = 'h';
        String i = "Hola";

        //Imprimiendo datos:
        System.out.println("Byte: " + a +"\nShort: "+b+"\nInt: "+c+"\nLong: "+d+"\nFloat: "+e+"\nDouble: "+f);
        System.out.println(g+"\nChar: " +h+"\nString: "+i);
    
    }
}
